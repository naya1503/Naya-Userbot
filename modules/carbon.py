@ayra_cmd(pattern="^[cC][a][r][b][o][n]")
async def crbn(event):
    from_user = vcmention(event.sender)
    xxxx = await eor(event, get_string("com_1"))
    te = event.text
    col = choice(all_col) if te[1] == "r" else "Grey"
    if event.reply_to_msg_id:
        temp = await event.get_reply_message()
        if temp.media:
            b = await event.client.download_media(temp)
            with open(b) as a:
                code = a.read()
            os.remove(b)
        else:
            code = temp.message
    else:
        try:
            code = event.text.split(" ", maxsplit=1)[1]
        except IndexError:
            return await eor(xxxx, get_string("carbon_2"), time=30)
    xx = await make_carbon(code)
    await xxxx.delete()
    await event.reply(
        get_string("carbon_1").format(from_user),
        file=xx,
    )
